/* Write a Java program that initializes an array of 5 integers with some
 values. Use a for loop to display each element of the array. */

public class QuestionNo_1 {
    public static void main(String[] args) {
        int arr[] = {2, 4, 6, 8, 10};
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}